package javapractice.Basic.Operator;

public class AssignmentOperator {

    public static void main(String[] args) {
        int a;
        a = 10; //simple Assignment
        System.out.println(a);

        a += 10; //Compound Assignment (a= a+10)
        System.out.println(a);

        a -= 10; //Compound Assignment (a= a-10)
        System.out.println(a);
    }
}