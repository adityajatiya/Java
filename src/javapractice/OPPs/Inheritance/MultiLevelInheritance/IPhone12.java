package javapractice.OPPs.Inheritance.MultiLevelInheritance;

class IPhone12 extends IPhone11 {

    public void camera() {
        System.out.println("Camera = 12MP");
    }

    public void ram() {
        System.out.println("Ram = 8GB");
    }

    public void rom() {
        System.out.println("Rom = 128GB");
    }

    public void price() {
        System.out.println(" Price = 100,000");
    }

    public void battery() {
        System.out.println("Battery = 5000 MAh");
    }
}
