class IPhone13 extends IPhone12 {

    public void camera() {
        System.out.println("Camera = 12MP");
    }

    public void ram() {
        System.out.println("Ram = 16GB");
    }

    public void rom() {
        System.out.println("Rom = 256GB");
    }

    public void price() {
        System.out.println(" Price = 120,000");
    }

    public void battery() {
        System.out.println("Battery = 5500 MAh");
    }
}
